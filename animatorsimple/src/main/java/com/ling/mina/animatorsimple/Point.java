public class Point {
    //定义两个float变量 用于记录坐标值
    private float x;
    private float y;
    public Point(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
